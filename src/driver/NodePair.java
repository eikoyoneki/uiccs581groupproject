package driver;

public class NodePair {
	private int x;
	private int y;
	
	public NodePair(int x, int y)
	{
		this.x=x;
		this.y=y;
	}
	
	public int getX()
	{
		return x;
	}

	public int getY()
	{
		return y;
	}
	
	public void setX(int x1)
	{
		x=x1;
	}
	
	public void setY(int y1)
	{
		y=y1;
	}
}
